def count_neighbours(grid, row, col):
    count = 0
    if grid[row][col] == 1:
        count -= 1
    for c in range(col-1, col+2):
        for r in range(row-1, row+2):
            if c == -1 or r == -1:
                count += 0
            else:
                try:
                    if grid[r][c] == 1:
                        count += 1
                except IndexError:
                    count += 0
    return count


